def _run_test(action: DrawRect, x_grids: list[Grid], y_grids: list[Grid]):
    x_shapes = [[Unknown(0, 0, grid)] for grid in x_grids]
    y_shapes = [[Unknown(0, 0, grid)] for grid in y_grids]
    state = create_test_state(x_shapes, y_shapes)
    program = AttentionExpertProgram(action, GlobalParams())
    result = program.run(state)
    assert result.out_shapes == y_shapes


