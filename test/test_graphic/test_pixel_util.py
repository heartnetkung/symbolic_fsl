def test_diff_pixels():
    x = [x117_0, x117_1, x117_2, x117_3]
    y = [y117_0, y117_1, y117_2, y117_3]
    for x_grid, y_grid in zip(x, y):
        assert set(diff_pixels(x_grid, y_grid)) == {(5, 8)}

    x = [x362_0, x362_1, x362_2]
    y = [y362_0, y362_1, y362_2]
    for x_grid, y_grid in zip(x, y):
        assert set(diff_pixels(x_grid, y_grid)) == {(0, 2)}

    x = [x69_0, x69_1, x69_2]
    y = [y69_0, y69_1, y69_2]
    for x_grid, y_grid in zip(x, y):
        assert set(diff_pixels(x_grid, y_grid)) == {(1, 3)}

    x = [x76_0, x76_1, x76_2]
    y = [y76_0, y76_1, y76_2]
    replacements = [(1, 4), (8, 4), (3, 4)]
    for x_grid, y_grid, rep in zip(x, y, replacements):
        assert set(diff_pixels(x_grid, y_grid)) == {rep}

    x = [x264_0, x264_1, x264_2]
    y = [y264_0, y264_1, y264_2]
    for x_grid, y_grid in zip(x, y):
        assert set(diff_pixels(x_grid, y_grid)) == {(0, 2)}

    x = [x161_0, x161_1, x161_2]
    y = [y161_0, y161_1, y161_2]
    for x_grid, y_grid in zip(x, y):
        assert set(diff_pixels(x_grid, y_grid)) == {(0, 1)}

    x = [x89_0, x89_1, x89_2]
    y = [y89_0, y89_1, y89_2]
    for x_grid, y_grid in zip(x, y):
        assert set(diff_pixels(x_grid, y_grid)) == {(0, 6)}

    x = [x62_0, x62_1, x62_2]
    y = [y62_0, y62_1, y62_2]
    for x_grid, y_grid in zip(x, y):
        assert set(diff_pixels(x_grid, y_grid)) == {(0, 3)}


